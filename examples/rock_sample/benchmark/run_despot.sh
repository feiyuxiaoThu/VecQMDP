#!/bin/bash
# ==============================================================================
# DESPOT Benchmark: 20 simulations with fixed rock bit configurations
# Generated rock bits: seed=42, randint(1, 32767), 15 rocks
# ==============================================================================

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"
BIN="${REPO_ROOT}/build/bin/Vec-QMDP_rock_sample"
LOG_DIR="${SCRIPT_DIR}/logs/despot_1000ms_512"

mkdir -p "${LOG_DIR}"

# 20 rock-bit configurations (shared with run_vecqmdp.sh, generated by seed=42)
ROCK_BITS=(
    20953   # sim 00  0b101000111011001
    3649    # sim 01  0b000111001000001
    820     # sim 02  0b000001100110100
    24300   # sim 03  0b101111011101100
    9013    # sim 04  0b010001100110101
    8025    # sim 05  0b001111101011001
    7315    # sim 06  0b001110010010011
    4573    # sim 07  0b001000111011101
    24133   # sim 08  0b101111001000101
    3359    # sim 09  0b000110100011111
    22175   # sim 10  0b101011010011111
    24271   # sim 11  0b101111011001111
    29235   # sim 12  0b111001000110011
    17871   # sim 13  0b100010111001111
    2849    # sim 14  0b000101100100001
    19350   # sim 15  0b100101110010110
    13826   # sim 16  0b011011000000010
    1042    # sim 17  0b000010000010010
    977     # sim 18  0b000001111010001
    3071    # sim 19  0b000101111111111
)

SIZE=15
NUM_ROCKS=15

NUM_SIMS=${#ROCK_BITS[@]}

echo "========================================"
echo " DESPOT Benchmark"
echo " Binary : ${BIN}"
echo " Sims   : ${NUM_SIMS}"
echo " LogDir : ${LOG_DIR}"
echo "========================================"
echo ""

if [[ ! -x "${BIN}" ]]; then
    echo "[ERROR] Binary not found or not executable: ${BIN}"
    echo "        Please build the project first (cmake --build build)"
    exit 1
fi

TOTAL_START=$(date +%s%N)

for i in $(seq 0 $((NUM_SIMS - 1))); do
    RB="${ROCK_BITS[$i]}"
    LOG="${LOG_DIR}/sim_$(printf '%02d' $i).log"

    SIM_START=$(date +%s%N)
    echo "[$(printf '%02d' $i)/${NUM_SIMS}] rock_bits=${RB}  =>  ${LOG}"

    "${BIN}" \
        --size ${SIZE} \
        --number ${NUM_ROCKS} \
        --rock-bits ${RB} \
        &> "${LOG}"

    SIM_END=$(date +%s%N)
    SIM_MS=$(( (SIM_END - SIM_START) / 1000000 ))
    echo "      done in ${SIM_MS} ms"
done

TOTAL_END=$(date +%s%N)
TOTAL_S=$(( (TOTAL_END - TOTAL_START) / 1000000000 ))

echo ""
echo "========================================"
echo " All ${NUM_SIMS} DESPOT simulations complete"
echo " Total elapsed: ${TOTAL_S}s"
echo " Run 'python3 examples/rock_sample/benchmark/analyze.py' to see results"
echo "========================================"
